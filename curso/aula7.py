# variaveis são usadas para armazenar valores na memória do computador
# variáveis são criadas usando o operador de atribuição (=)
# o nome da variável deve seguir as regras de nomenclatura de variáveis em Python
# variáveis são case-sensitive, ou seja, nome e Nome são variáveis diferentes
# variáveis podem ser de diferentes tipos de dados, como str, int, float, bool
nome = 'João' # str
idade = 30 # int
altura = 1.75 # float
estudante = True # bool
print(nome) # João
print(idade) # 30
print(altura) # 1.75
print(estudante) # True