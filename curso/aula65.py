"""
Introdução às funções (def) em Python
Funções são trechos de código que podem ser reutilizados em diferentes partes do programa.
Elas ajudam a organizar o código, tornando-o mais legível e fácil de manter.
Em Python, as funções são definidas usando a palavra-chave `def`, seguida pelo nome da função e parênteses que podem conter parâmetros.
"""

def numero():
    """
    Função que retorna um número inteiro.
    """
    print(42)
numero()  # Chamada da função, que imprime o número 42